<?
//@ Menginisialisasi proyek:
//? Perintah lengkap: npm init
//? Perintah singkat: npm init -y (menggunakan nilai default untuk semua pertanyaan)

//@ Menginstal paket:
//? Perintah lengkap: npm install nama_paket
//? Perintah singkat: npm i nama_paket

//@ Menginstal paket dan menyimpannya sebagai dependensi:
//? Perintah lengkap: npm install nama_paket --save
//? Perintah singkat: npm i nama_paket -S

//@ Menginstal paket dan menyimpannya sebagai dependensi pengembangan:
//? Perintah lengkap: npm install nama_paket --save-dev
//? Perintah singkat: npm i nama_paket -D

//@ Menghapus paket:
//? Perintah lengkap: npm uninstall nama_paket
//? Perintah singkat: npm un nama_paket

//@ Menjalankan skrip:
//? Perintah lengkap: npm run nama_skrip
//? Perintah singkat: npm run nama_skrip

//@ Menjalankan skrip secara paralel:
//? Perintah lengkap: npm run skrip1 & npm run skrip2
//? Perintah singkat: npm-run-all skrip1 skrip2 (membutuhkan paket npm-run-all)

//@ Memperbarui paket:
//? Perintah lengkap: npm update nama_paket
//? Perintah singkat: npm update nama_paket

//@ Mencari paket:
//? Perintah lengkap: npm search kata_kunci
//? Perintah singkat: npm search kata_kunci

//@ Melihat daftar paket yang terinstal:
//? Perintah lengkap: npm list
//? Perintah singkat: npm list

//@ Melihat informasi paket:
//? Perintah lengkap: npm show nama_paket
//? Perintah singkat: npm show nama_paket